    private void equationTypeComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_equationTypeComboBoxItemStateChanged
        switch(String.valueOf(equationTypeComboBox.getSelectedItem())){
            case "aX+b=0":
                equationState = EquationState.linear;
                
                equationPanel.removeAll();
                showEquationComponent(EquationState.linear);
                equationPanel.repaint();
                
                break;
                
            case "aX²+bX+c=0":
                equationState = EquationState.quadra;
                
                equationPanel.removeAll();
                showEquationComponent(EquationState.quadra);
                equationPanel.repaint();
                
                break;
                
            case "aX+bY=c":
                equationState = EquationState.doubleLinear;
                
                equationPanel.removeAll();
                showEquationComponent(EquationState.doubleLinear);
                equationPanel.repaint();
                
                break;
                
            case "aX+bY+cZ=d":
                equationState = EquationState.tripleLinear;
                
                equationPanel.removeAll();
                showEquationComponent(EquationState.tripleLinear);
                equationPanel.repaint();
                
                break;
                
            default:
                    break;
        }
    }//GEN-LAST:event_equationTypeComboBoxItemStateChanged

