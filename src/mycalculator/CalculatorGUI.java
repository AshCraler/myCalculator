    private void showEquationComponent(EquationState eState){
        switch(eState){
            case linear :
                addLabel(typeLabel, 106, 25, 0, 20);
                typeLabel.setText("aX+b=0");
                
                addLabel(aLabel, 25, 25, 10, 50);
                addLabel(bLabel, 25, 25, 10, 80);
                
                addTextField(aTextField, 50, 25, 40, 50);
                addTextField(bTextField, 50, 25, 40, 80);
                
                addButton(solveButton, 100, 30, 70, 120);
                break;
                
            case quadra:
                addLabel(typeLabel, 150, 25, 0, 20);
                typeLabel.setText("aX²+bX+c=0");
                
                addLabel(aLabel, 25, 25, 10, 50);
                addLabel(bLabel, 25, 25, 10, 80);
                addLabel(cLabel, 25, 25, 10, 110);
                
                addTextField(aTextField, 50, 25, 40, 50);
                addTextField(bTextField, 50, 25, 40, 80);
                addTextField(cTextField, 50, 25, 40, 110);
                
                addButton(solveButton, 100, 30, 70, 150);
                break;
                
            case doubleLinear:
                equationPanel.add(doubleLinearImgPanel);
                doubleLinearImgPanel.setSize(175, 75);
                doubleLinearImgPanel.setLocation(0, 20);
                
                //first equation
                addTextField(aTextField, 40, 25, 0, 105);
                addLabel(x1Label, 30, 25, 40, 105);
                addTextField(bTextField, 40, 25, 70, 105);
                addLabel(y1Label, 30, 25, 110, 105);y1Label.setText("Y = ");
                addTextField(cTextField, 50, 25, 150, 105);
                
                //second equation
                addTextField(a2TextField, 40, 25, 0, 135);
                addLabel(x2Label, 30, 25, 40, 135);
                addTextField(b2TextField, 40, 25, 70, 135);
                addLabel(y2Label, 30, 25, 110, 135);y2Label.setText("Y = ");
                addTextField(c2TextField, 50, 25, 150, 135);
                
                addButton(solveButton, 100, 30, 70, 170);
                
                break;
                
            case tripleLinear:
                equationPanel.add(tripleLinearImgPanel);
                tripleLinearImgPanel.setSize(175, 100);
                tripleLinearImgPanel.setLocation(0, 20);
                
                //first equation
                addTextField(aTextField, 40, 25, 0, 125);
                addLabel(x1Label, 30, 25, 40, 125);
                addTextField(bTextField, 40, 25, 70, 125);
                addLabel(y1Label, 30, 25, 110, 125); y1Label.setText("Y + ");
                addTextField(cTextField, 40, 25, 140, 125);
                addLabel(z1Label, 40, 25, 180, 125); 
                addTextField(d1TextField, 50, 25, 220, 125);
                
                //second equation
                addTextField(a2TextField, 40, 25, 0, 155);
                addLabel(x2Label, 30, 25, 40, 155);
                addTextField(b2TextField, 40, 25, 70, 155);
                addLabel(y2Label, 30, 25, 110, 155); y2Label.setText("Y + ");
                addTextField(c2TextField, 40, 25, 140, 155);
                addLabel(z2Label, 40, 25, 180, 155); 
                addTextField(d2TextField, 50, 25, 220, 155);
                
                //third equation
                addTextField(a3TextField, 40, 25, 0, 185);
                addLabel(x3Label, 30, 25, 40, 185);
                addTextField(b3TextField, 40, 25, 70, 185);
                addLabel(y3Label, 30, 25, 110, 185); y3Label.setText("Y + ");
                addTextField(c3TextField, 40, 25, 140, 185);
                addLabel(z3Label, 40, 25, 180, 185); 
                addTextField(d3TextField, 50, 25, 220, 185);
                
                addButton(solveButton, 100, 30, 70, 230);
                break;
                
            default:
                break;
        }
    }
