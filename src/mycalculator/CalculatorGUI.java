    private void delPanelButtonMousePressed(MouseEvent evt, int index) {

        gp.removeFunction(index);
        
        for(int i = index; i < funcCount - 1; i++){

            functionPanels[i].changeTo(functionPanels[i+1]);
//            functionPanels[i].setLocation(5, 150 + 100*i);
            functionPanels[i].setLocation(5, 10 + 100*i);
        }

//        this.remove(functionPanels[funcCount - 1]);
        functionPanel.remove(functionPanels[funcCount - 1]);
        functionPanels[funcCount - 1] = null;
        
        funcCount--;
        
        repaint();
    }
    
    private void drawButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_drawButtonMousePressed
        switch(functionType){
            case linear:
                if(aTextField.getText() == "" || bTextField.getText() == ""){
                    JPanel panel = (JPanel) getContentPane();
                    JOptionPane.showMessageDialog(panel, "Vui long nhap cac he so hop le!","Error", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                    
                linearFunction lF = new linearFunction(Float.parseFloat(aTextField.getText()), Float.parseFloat(bTextField.getText()));
                
                gp.addFunction(lF);
                
                functionPanels[funcCount] = new FunctionPanel(lF, funcCount, gp);
                
//                addFP(functionPanels[funcCount], 5, 150 + 100*funcCount);
                addFP(functionPanels[funcCount], 5, 10 + 100*funcCount);
                funcCount++;
                
                break;
                
            case circle:
                if(aTextField.getText() == "" || bTextField.getText() == "" || radiusTextField.getText() == ""){
                    JPanel panel = (JPanel) getContentPane();
                    JOptionPane.showMessageDialog(panel, "Vui long nhap cac he so hop le!", "Error", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                circleFunction cF = new circleFunction(Float.parseFloat(aTextField.getText()), Float.parseFloat(bTextField.getText()), Float.parseFloat(radiusTextField.getText()));
                
                gp.addFunction(cF);
                
                functionPanels[funcCount] = new FunctionPanel(cF, funcCount, gp);
                
//                addFP(functionPanels[funcCount], 5, 150 + 100*funcCount);
                addFP(functionPanels[funcCount], 5, 10 + 100*funcCount);

                funcCount++;
                break;
            case parabol:
                if(aTextField.getText() == "" || bTextField.getText() == "" || cTextField.getText() == ""){
                    JPanel panel = (JPanel) getContentPane();
                    JOptionPane.showMessageDialog(panel, "Vui long nhap cac he so hop le!", "Error", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                parabolFunction pF = new parabolFunction(Float.parseFloat(aTextField.getText()), Float.parseFloat(bTextField.getText()), Float.parseFloat(cTextField.getText()));
                
                gp.addFunction(pF);
                
                functionPanels[funcCount] = new FunctionPanel(pF, funcCount, gp);
                
//                addFP(functionPanels[funcCount], 5, 150 + 100*funcCount);
                addFP(functionPanels[funcCount], 5, 10 + 100*funcCount);

                funcCount++;
                break;
            
            default:
                break;
        }
        
        backButtonMousePressed(evt);
    }//GEN-LAST:event_drawButtonMousePressed

