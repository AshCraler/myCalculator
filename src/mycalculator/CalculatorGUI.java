    private void solveButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_solveButtonMouseClicked
        switch(equationState){
            case linear:
                Temp[0] = Float.parseFloat(CalculatorObjects.Operation.equal(aTextField.getText()));
                Temp[1] = Float.parseFloat(CalculatorObjects.Operation.equal(bTextField.getText()));
                
                tempLinear = new CalculatorObjects.linearEquation(Temp[0], Temp[1]);
                if(tempLinear.isSolvable){
                    resultLabel.setText("X = " + String.valueOf(tempLinear.getSolution()));
                }else
                    resultLabel.setText("Phuong trinh nay vo nghiem!");
                
                addLabel(resultLabel, 100, 30, 50, 180);
                break;
                
            case quadra:
                Temp[0] = Float.parseFloat(CalculatorObjects.Operation.equal(aTextField.getText()));
                Temp[1] = Float.parseFloat(CalculatorObjects.Operation.equal(bTextField.getText()));
                Temp[2] = Float.parseFloat(CalculatorObjects.Operation.equal(cTextField.getText()));
                
                tempQuadra = new CalculatorObjects.quadraticEquation(Temp[0], Temp[1], Temp[2]);
                
                if(tempQuadra.isSolvable){
                    if(tempQuadra.isSingleSolution)
                        resultLabel.setText(CalculatorObjects.convertToMultiline("Phuong trinh co nghiem kep: \nX1 = X2 = " + String.valueOf(tempQuadra.getSolutions()[0])));
                    else
                        resultLabel.setText(CalculatorObjects.convertToMultiline("Phuong trinh co 2 nghiem phan biet: \nX1 = " + String.valueOf(tempQuadra.getSolutions()[0]) + "\nX2 = "+ String.valueOf(tempQuadra.getSolutions()[1])));
                }
                else
                    resultLabel.setText("Phuong trinh nay vo nghiem!");
                
                addLabel(resultLabel, 150, 150, 50, 180);
                break;
                
            case doubleLinear:
                Temp[0] = Float.parseFloat(CalculatorObjects.Operation.equal(aTextField.getText()));
                Temp[1] = Float.parseFloat(CalculatorObjects.Operation.equal(bTextField.getText()));
                Temp[2] = Float.parseFloat(CalculatorObjects.Operation.equal(cTextField.getText()));
                Temp[3] = Float.parseFloat(CalculatorObjects.Operation.equal(a2TextField.getText()));
                Temp[4] = Float.parseFloat(CalculatorObjects.Operation.equal(b2TextField.getText()));
                Temp[5] = Float.parseFloat(CalculatorObjects.Operation.equal(c2TextField.getText()));
                
                tempDouble = new CalculatorObjects.doubleLinearEquation(Temp[0], Temp[1], Temp[2], Temp[3], Temp[4], Temp[5]);
                
                switch(tempDouble.getState()){
                    case vonghiem:
                        resultLabel.setText("He phuong trinh vo nghiem!");
                        break;
                        
                    case nghiemdon:
                        resultLabel.setText(CalculatorObjects.convertToMultiline("He phuong trinh co nghiem duy nhat la: \nX = " + tempDouble.getX() + "\nY = " + tempDouble.getY()));
                        break;
                    
                    case vosonghiem:
                        resultLabel.setText("He phuong trinh vo so nghiem!");
                        break;
                        
                    default:
                        break;
                }
                
                addLabel(resultLabel, 250, 150, 20, 180);
                break;
                
            case tripleLinear:
                break;
                
            default:
                break;
        }
        
        
        
        
        
    }//GEN-LAST:event_solveButtonMouseClicked

