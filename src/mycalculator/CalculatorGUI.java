    private void input(String str){  
        operationString += str;
        
        switch(appState){
            case calculating:
                resultTextPane.setText(operationString);
                break;
            
            case equationing:
                currentField.setText(operationString);
                break;
                
            case graphing:
                resultTextPane.setText(operationString);
                break;
                
                default:
                    break;
        }
    }
    
